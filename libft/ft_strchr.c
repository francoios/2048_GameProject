#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (s)
	{
		while (*s != (char)c)
		{
			if (*s == '\0')
				return (NULL);
			s++;
		}
		return ((char*)s);
	}
	return (NULL);
}
