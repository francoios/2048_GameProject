#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int	len;
	int	i;

	if (s && f)
	{
		len = ft_strlen(s);
		i = 0;
		while (i < len)
		{
			(*f)(s + i);
			i++;
		}
	}
}
