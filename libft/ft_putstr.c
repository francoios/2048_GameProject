#include "libft.h"

void	ft_putstr(char *str)
{
	int		i;

	i = 0;
	if (str == NULL)
	{
		ft_putstr("(null)");
	}
	else
	{
		while (str[i] != '\0')
		{
			ft_putchar(str[i]);
			i++;
		}
	}
}
